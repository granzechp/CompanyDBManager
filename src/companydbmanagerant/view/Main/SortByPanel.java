    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int[] selectedRows = EmployeeTable.getSelectedRows();
        if (selectedRows.length == 1) {
            boolean success = sortByTableModel.upper(selectedRows[0]);

            sortByTableModel.refreshOrder();
            sortByTableModel.fireTableDataChanged();
            if (success) {
                EmployeeTable.setRowSelectionInterval(selectedRows[0] - 1, selectedRows[0] - 1);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

