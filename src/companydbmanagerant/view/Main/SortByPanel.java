    private void setTableListener() {
        // 테이블의 selection 모델에 대한 ListSelectionListener 추가
        EmployeeTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                // getValueIsAdjusting는 이벤트가 한 번만 처리되도록 해줌
                if (!e.getValueIsAdjusting()) {
                    // 선택된 행의 개수 확인
                    int[] selectedRows = EmployeeTable.getSelectedRows();

                    // 선택된 행이 정확히 하나일 경우 버튼을 활성화
                    boolean enableButtons = selectedRows.length == 1 || selectedRows.length == 0;
                    jButton3.setEnabled(enableButtons);
                    jButton4.setEnabled(enableButtons);

                    if (selectedRows.length == 1) {

                        if (selectedRows[0] == 0) {
                            jButton3.setEnabled(false);
                        }
                        if (selectedRows[0] == sortByTableModel.getRowCount() - 1) {
                            jButton4.setEnabled(false);
                        }
                        int noneindex = sortByTableModel.findNone();
                        if (noneindex != -1 && noneindex <= selectedRows[0]) {
                            jButton3.setEnabled(false);
                        }
                        if (noneindex != -1 && noneindex <= selectedRows[0] + 1) {
                            jButton4.setEnabled(false);
                        }
                    }

                    boolean enableButtons2 = selectedRows.length != 0;
                    jButton5.setEnabled(enableButtons2);
                    jButton6.setEnabled(enableButtons2);
                    jButton7.setEnabled(enableButtons2);
                }
            }
        });

    }

